package violations;

public class Book
{
    String bookName;
    int publishYear;
    int bookPrice;
    String authorName;

    public Book(String bookName, int publishYear, int bookPrice, String authorName)
    {
        this.bookName = bookName;
        this.publishYear=publishYear;
        this.bookPrice = bookPrice;
        this.authorName = authorName;
    }
}
